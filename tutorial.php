            <p><span>Thread group:</span> is to set up number of user to run the test.</p>
            <p><span>Sampler:</span> is the request for running the test (http/https/soap/rest).</p>
            <p><span>Assertions:</span> are to use for comparing the actual result with the expected result so that we
