            <h3>Jmeter Fundamentals, Install and Create A Basic Jmeter Test Plan </h3>
            <h4>Load, Stress And Performance Test</h4>
            <div>
                <p><span>Load test:</span> is the testing how the system works by increasing the load sending to the
                    system. With load testing, we will know how many user using software product that the system still
                    works properly.</p>
                <p><span>Performance test:</span> is the testing how software application performed such as how long it
                    takes for the servers to response. With performance testing, we will know how long it takes for a
                    software product get response so that we can improve the quality of software by making sure it will
                    be run in the expected time response.</p>
                <p><span>Stress test:</span> is the testing how the system works under stress. With stress test, we will
                    know at which point the system will be failed and how the system recovers back.</p>
            </div>
            <div>
                <h4>What Is Jmeter?</h4>
                <p><span>Jmeter</span> is a free application testing software from The Apache Software Foundation. </p>
                <p><span>Jmeter</span> is a tool for running Load test, Performance test or Stress test. It is designed
                    for testing Web Application, Web services (Soap & Rest api) and more.</p>
            </div>
            <div>
                <h4>How does Jmeter work?</h4>
                <p><span>Jmeter</span> acts like a group of users sending requests to a target server. It collects
                    response from target server and other information which show the performance of the application via
                    graphs or summary tables.</p>
            </div>
            <div>
                <h4>Why Use Jmeter?</h4>
                <p><span>Jmeter </span>is an open-source application. Therefore, it is very popular Test tool.</p>
                <p><span>Jmeter</span> is one of the top Software Testing Tool for automation. Most of the opening job
                    for software tester, QA engineer, Automation analyst, QA Analyst require or “good to have” Jmeter
                    skill.</p>
            </div>
